typedef struct elemento{
    int token;
    struct elemento *prox;
}No;

typedef struct Fila {
    int tamanho;
    No* primeiro;
    No* ultimo;
}Fila;

void iniciaFila(Fila* fila){
    fila->tamanho = 0;
    fila->primeiro = NULL;
    fila->ultimo = NULL;
}

void push(Fila* fila, int token) {
    if (fila->tamanho == 0) {
        fila->primeiro = (No*)malloc(sizeof(No));
        fila->primeiro->token = token;
        fila->ultimo = fila->primeiro;
        fila->tamanho++;
    }
    else {
        fila->ultimo->prox = (No*)malloc(sizeof(No));
        fila->ultimo->prox->token = token;
        fila->ultimo->prox->prox = NULL;
        fila->ultimo = fila->ultimo->prox;
        fila->tamanho++;
    }
}

int pull(Fila* fila) {
    int token;
    No* aux;
    
    token = -1;
    
    if (fila->tamanho == 0) {
        return -1;
    }
    else {
        token = fila->primeiro->token;
        aux = fila->primeiro;
        fila->primeiro = fila->primeiro->prox;
        free(aux);
        fila->tamanho--;
        return token;
    }
    return -1;
}


